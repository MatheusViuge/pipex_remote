#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	print;
	long	nb;

	nb = n;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		nb = nb % 10;
	}
	print = nb + 48;
	ft_putchar_fd(print, fd);
}
